"""
Ansible module for validating nginx configuration files.

The module executes ``nginx -t`` and evaluates the returned stderr output,
because nginx writes validation messages to stderr by design.

The module intentionally never raises an Ansible failure for invalid nginx
configurations. Instead, the validation result is returned to the caller,
allowing roles and playbooks to decide how to proceed.
"""

from __future__ import annotations

import re
from typing import Any, Dict, List, Tuple

from ansible.module_utils.basic import AnsibleModule

# ---------------------------------------------------------------------------

DOCUMENTATION = r"""
---
module: nginx_verify_config

short_description: Validate nginx configuration syntax.

version_added: "1.2.0"

author:
  - "Bodo Schulz (@bodsch) <me+ansible@bodsch.me>"

description:
  - Executes C(nginx -t) to validate the nginx configuration.
  - Parses the stderr output generated by nginx.
  - Returns the validation result without failing the module execution.

notes:
  - nginx writes validation information to stderr.
  - This module never modifies system state.
"""

EXAMPLES = r"""
- name: Validate nginx configuration
  nginx_verify_config:
  register: nginx_config_check

- name: Print nginx validation output
  ansible.builtin.debug:
    var: nginx_config_check
"""

RETURN = r"""
changed:
  description:
    - Always C(false).
  type: bool
  returned: always

failed:
  description:
    - Indicates whether nginx validation failed.
  type: bool
  returned: always

rc:
  description:
    - Return code from C(nginx -t).
  type: int
  returned: always

msg:
  description:
    - Raw stderr output from nginx validation.
  type: str
  returned: always

valid:
  description:
    - Indicates whether the nginx configuration syntax is valid.
  type: bool
  returned: always
"""

# ---------------------------------------------------------------------------


_SYNTAX_OK_PATTERN = re.compile(
    r"syntax is ok",
    re.IGNORECASE,
)

_TEST_SUCCESS_PATTERN = re.compile(
    r"test is successful",
    re.IGNORECASE,
)


class NginxVerifyConfig:
    """
    Validate nginx configuration syntax.

    The class executes ``nginx -t`` and evaluates the stderr output
    produced by nginx.
    """

    def __init__(self, module: AnsibleModule) -> None:
        """
        Initialize the validator instance.

        Args:
            module:
                Initialized Ansible module instance.
        """
        self.module = module
        self.nginx_bin: str = module.get_bin_path("nginx", required=True)

    # ------------------------------------------------------------------
    # Public entry point
    # ------------------------------------------------------------------

    def run(self) -> Dict[str, Any]:
        """
        Execute nginx configuration validation.

        Returns:
            Stable Ansible result dictionary.
        """
        rc, _, stderr = self.__exec([self.nginx_bin, "-t"])

        self.module.log(f" stderr: {stderr}")

        valid = self._is_valid(stderr)

        result = self._build_result(
            rc=rc,
            stderr=stderr,
            valid=valid,
        )

        self.module.log(f"= result: {result}")

        return result

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def __exec(
        self,
        commands: List[str],
        check_rc: bool = False,
    ) -> Tuple[int, str, str]:
        """Run an external command via the Ansible module helper.

        All output is forwarded to the Ansible module log for debugging.

        Args:
            commands: Argv list to execute.
            check_rc: When ``True`` Ansible raises on non-zero exit codes.
                Defaults to ``False`` so that a missing or broken nginx
                binary is handled gracefully by the caller.

        Returns:
            A 3-tuple ``(rc, stdout, stderr)``.
        """
        rc, out, err = self.module.run_command(commands, check_rc=check_rc)

        return rc, out, err

    @staticmethod
    def _is_valid(stderr: str) -> bool:
        """
        Evaluate nginx validation output.

        Args:
            stderr:
                Stderr output returned by nginx.

        Returns:
            True when nginx reported a valid configuration.
        """
        normalized = stderr.strip()

        syntax_ok = _SYNTAX_OK_PATTERN.search(normalized)
        test_successful = _TEST_SUCCESS_PATTERN.search(normalized)

        return bool(syntax_ok and test_successful)

    @staticmethod
    def _build_result(
        rc: int,
        stderr: str,
        valid: bool,
    ) -> dict[str, Any]:
        """
        Build the final Ansible result structure.

        Args:
            rc:
                Command return code.

            stderr:
                Raw stderr output.

            valid:
                Validation status.

        Returns:
            Stable Ansible result dictionary.
        """
        return {
            "changed": False,
            "failed": False,
            "rc": rc,
            "valid": valid,
            "msg": stderr.strip(),
        }


# ---------------------------------------------------------------------------


def main() -> None:
    """
    Ansible module entry point.
    """
    module = AnsibleModule(argument_spec={}, supports_check_mode=True)

    validator = NginxVerifyConfig(module)

    module.exit_json(**validator.run())


if __name__ == "__main__":
    main()
