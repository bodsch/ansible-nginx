# ---------------------------------------------------------------------------

#: Directory used to persist per-run checksums.
CACHE_DIRECTORY: str = "/var/cache/ansible/nginx"

# Jinja2 template for the global ssl.conf snippet.
# ``trim_blocks`` and ``lstrip_blocks`` are set on the Template object so
# the whitespace directives in the template body are honoured correctly.
_TPL_SSL = """\
# generated by ansible

{% for key, value in item.items() %}
{% if key == "ssl_protocols" %}
{{ key.ljust(24) }}  {{ value | join(' ') }};
{% elif value is sameas true or value is sameas false %}
{{ key.ljust(24) }}  {{ 'on' if value else 'off' }};
{% else %}
{{ key.ljust(24) }}  {{ value }};
{% endif %}
{% endfor %}
"""

# Jinja2 template for a single ssl_<name>.conf cipher snippet.
_TPL_CIPHERS = """\
# generated by ansible

ssl_ciphers  {{ item }};
"""


class NginxSsl:
    """Render nginx SSL configuration snippets from a structured dict.

    Attributes:
        module: The :class:`AnsibleModule` instance provided by Ansible.
        ssl_config: Raw SSL configuration dict from the Ansible parameter.
        dest: Target directory for rendered ``.conf`` files.
        checksum: :class:`Checksum` helper, initialised in :meth:`__init__`.
    """

    def __init__(self, module: AnsibleModule) -> None:
        """Initialise from Ansible module parameters.

        Args:
            module: Fully initialised :class:`AnsibleModule` instance.
        """
        self.module = module
        self.ssl_config: Dict[str, Any] = module.params["config"]
        self.dest: str = module.params.get("dest") or "/etc/nginx/includes.d"
        self.checksum = Checksum(module)

    # ------------------------------------------------------------------
    # Public entry point
    # ------------------------------------------------------------------

    def run(self) -> Dict[str, Any]:
        """Render SSL config snippets when the configuration has changed.

        Returns:
            Ansible result dict with keys ``changed``, ``failed``, ``result``.
        """
        create_directory(CACHE_DIRECTORY)

        global_checksum_file = os.path.join(CACHE_DIRECTORY, "ssl_config")
        changed, _checksum, _old = self.checksum.validate(
            checksum_file=global_checksum_file,
            data=self.ssl_config,
        )

        if not changed:
            return dict(
                changed=False,
                failed=False,
                msg="The ssl configuration has not been changed.",
            )

        result_state: List[Dict[str, Any]] = []

        if not isinstance(self.ssl_config, dict):
            return dict(
                changed=False,
                failed=True,
                msg="ssl config must be a dict.",
            )

        # Work on a shallow copy so we do not mutate the caller's data.
        ssl_config = self.ssl_config.copy()
        ssl_config.pop("enabled", None)
        ssl_ciphers: Dict[str, Any] = ssl_config.pop("ssl_ciphers", {})

        # ---- global ssl.conf ----
        file_name = "ssl.conf"
        _failed, _changed, _msg = self._write_template(
            template_type="ssl",
            file_path=os.path.join(self.dest, file_name),
            data=ssl_config,
        )
        result_state.append(
            {file_name: dict(failed=_failed, changed=_changed, msg=_msg)}
        )

        # ---- per-suite ssl_<name>.conf ----
        for suite_name, cipher_list in ssl_ciphers.items():
            file_name = f"ssl_{suite_name}.conf"
            _failed, _changed, _msg = self._write_template(
                template_type="ssl_ciphers",
                file_path=os.path.join(self.dest, file_name),
                data=cipher_list,
            )
            result_state.append(
                {file_name: dict(failed=_failed, changed=_changed, msg=_msg)}
            )

        _state, _changed, _failed, state, changed, failed = results(
            self.module, result_state
        )

        return dict(changed=_changed, failed=_failed, result=result_state)

    # ------------------------------------------------------------------
    # Private helpers
    # ------------------------------------------------------------------

    def _write_template(
        self,
        template_type: str,
        file_path: str,
        data: Any,
    ) -> Tuple[bool, bool, str]:
        """Render a Jinja2 template and write it to *file_path* if changed.

        The method normalises *data* to the form expected by each template:

        * For the ``ssl`` template, *data* must be a dict.  It is
          JSON-round-tripped with ``sort_keys=True`` to produce a
          stable key order before checksum comparison.
        * For the ``ssl_ciphers`` template, *data* must be a list of
          cipher strings that are joined with ``":"`` into a single
          ``ssl_ciphers`` directive value.

        A per-file checksum stored in :data:`CACHE_DIRECTORY` prevents
        unnecessary writes when the rendered content is unchanged.

        Args:
            template_type: Either ``"ssl"`` or ``"ssl_ciphers"``.
            file_path: Absolute destination path for the rendered file.
            data: Configuration payload for the template.

        Returns:
            A 3-tuple ``(failed, changed, message)``.
        """
        # Normalise data for stable checksumming and rendering.
        if isinstance(data, dict):
            # Round-trip through JSON with sorted keys to get a canonical
            # representation.  The result is always a dict after loads().
            data = json.loads(json.dumps(data, sort_keys=True))
        elif isinstance(data, list):
            data = ":".join(data)

        checksum_file = os.path.join(
            CACHE_DIRECTORY,
            f"{os.path.basename(file_path)}.checksum",
        )

        changed, checksum, _old = self.checksum.validate(
            checksum_file=checksum_file,
            data=data,
        )

        if not changed:
            return False, False, "The configuration file has not been changed."

        tpl_source = _TPL_SSL if template_type == "ssl" else _TPL_CIPHERS
        template = Template(tpl_source, trim_blocks=True, lstrip_blocks=True)
        rendered = template.render(item=data)

        with open(file_path, "w") as fh:
            fh.write(rendered)

        self.checksum.write_checksum(checksum_file=checksum_file, checksum=checksum)

        return False, True, "The configuration file was written successfully."


# ---------------------------------------------------------------------------


def main() -> None:
    """Module entry point.  Called by Ansible at runtime."""
    args: Dict[str, Any] = dict(
        config=dict(required=True, type="dict"),
        dest=dict(required=False, default="/etc/nginx/includes.d"),
    )

    module = AnsibleModule(argument_spec=args, supports_check_mode=True)

    p = NginxSsl(module)
    result = p.run()

    module.log(msg=f"= result: {result}")
    module.exit_json(**result)


if __name__ == "__main__":
    main()
