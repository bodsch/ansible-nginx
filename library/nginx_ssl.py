"""
Ansible module ``nginx_ssl`` – render nginx SSL / TLS configuration snippets.

This module converts a structured SSL configuration dict into one or more
nginx ``include``-able ``.conf`` snippets and writes them to *dest*.

Template overview
-----------------
``ssl.conf``
    Contains all global SSL directives (protocols, session timeouts, HSTS,
    etc.).  Keys whose value is a Python bool are rendered as nginx ``on`` /
    ``off`` literals.  ``ssl_protocols`` is rendered as a space-joined list.

``ssl_<name>.conf``  (one per cipher suite group)
    Contains a single ``ssl_ciphers`` directive.

Checksum-based idempotency
--------------------------
A SHA-256 checksum of the entire configuration dict is stored in
:data:`CACHE_DIRECTORY`.  The module returns ``changed=False`` immediately
when the checksum matches the previous run, avoiding unnecessary file writes.
Each individual template file also has its own checksum so that only
modified cipher files are re-rendered.
"""

from __future__ import absolute_import, annotations, print_function

import json
import os
from typing import Any, Dict, List, Tuple

from ansible.module_utils.basic import AnsibleModule
from ansible_collections.bodsch.core.plugins.module_utils.checksum import Checksum
from ansible_collections.bodsch.core.plugins.module_utils.directory import (
    create_directory,
)
from ansible_collections.bodsch.core.plugins.module_utils.module_results import results
from jinja2 import Template

# ---------------------------------------------------------------------------

